import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          {
            role: "system",
            content: `You are an advanced AI assistant specializing in cybersecurity, ethical hacking, penetration testing, and information security. You provide detailed, educational, and technical information to help users understand security concepts, vulnerabilities, and defensive measures.

Your expertise includes:
- Network security and protocols
- Web application security (OWASP Top 10)
- System hardening and security best practices
- Penetration testing methodologies
- Vulnerability assessment and exploitation techniques
- Cryptography and encryption
- Malware analysis and reverse engineering
- Social engineering defense
- Security tools and frameworks (Metasploit, Burp Suite, Wireshark, Nmap, etc.)
- Security certifications (CEH, OSCP, CISSP)
- Incident response and forensics

Important guidelines:
- Provide educational content to help users learn about security
- Explain both offensive and defensive security concepts
- Include practical examples and real-world scenarios
- Discuss ethical considerations and legal boundaries
- Emphasize the importance of authorization before testing
- Help users understand security from both attacker and defender perspectives
- Provide detailed technical explanations when requested
- Support learning and skill development in cybersecurity

Always maintain an educational focus and promote ethical, legal, and authorized security practices.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limits exceeded, please try again later." }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }),
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Cybersecurity AI error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
